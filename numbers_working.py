# Integers
print(2 + 3)
print(2 * 3)
print(2 - 3)
print(2 / 3)
print(2 ** 3)

# Floats
print(0.1 + 0.1)
print(0.1 * 0.1)
print(2 - 0.1)
print(0.1 / 0.01)
print(0.1 ** 0.1)

# Integers and Floats
print(1 + 0.2) # operation of float and integer will always return a float value

# Underscores in Numbers
universe_age = 14_000_000_000
print(universe_age)

# Multiple assignment
x, y , z = 0, 0, 0
print(x)
print(y)
print(z)

